function openQV(url,params,fullscreen){
	windowobj = window.open(url,"QV",params);
	if (fullscreen) {
		windowobj.moveTo(0,0);
		windowobj.resizeTo(screen.availWidth, screen.availHeight);
	}
	windowobj.focus();
	return false;
}
